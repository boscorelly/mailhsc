function detectLang() {
  var langs = navigator.languages || [navigator.language || 'en'];
  for (var i = 0; i < langs.length; i++) {
    var code = langs[i].split('-')[0].toLowerCase();
    if (TRANSLATIONS[code]) return code;
  }
  return 'en';
}

